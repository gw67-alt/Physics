# Given values and assumptions
B = 0.17  # Tesla (magnetic field strength)
v = 0.5   # m/s (velocity of magnet)
F = 0.00981     # Newton (assumed force)

# Calculating power generated using the simplified formula
P = B * v * F
print(P, "watts per gram.")  # power in watts

